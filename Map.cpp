Map::Map()
{
	dirt = TextureManager::LoadTexture("Assets/Map/Ground/Dirt.png"); // su texture manager sukuriama teksturas
	grass = TextureManager::LoadTexture("Assets/Map/Ground/Grass.png");
	water = TextureManager::LoadTexture("Assets/Map/Ground/Water.png");

	LoadMap(lvl1); //zemelapis užkraunamas, jam priduodamas levelis
	src.x = src.y = 0; //zemelapio vieta
	src.w = dest.w = 32;
	src.h = dest.h = 32;

	dest.x = dest.y = 0;
}

Map::~Map()
{
}

void Map::LoadMap(int arr[20][25]) //Žemelapio krovimas
{
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			map[row][column] = arr[row][column];
		}
	}
}

void Map::DrawMap() //Ženmelapio piesimas
{
	int type = 0;
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			type = map[row][column];
			dest.x = column * 32;
			dest.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(water, src, dest);
				break;
			case 1:
				TextureManager::Draw(grass, src, dest);
				break;
			case 2:
				TextureManager::Draw(dirt, src, dest);
				break;

			default:
				break;
			}
		}
	}
}
